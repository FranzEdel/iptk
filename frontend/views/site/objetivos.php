	<h1>Objetivos Estratégicos</h1>

	<p>Los Objetivos Estratégicos Institucionales planteados por el IPTK son:</p>

	<h4>CONSTRUCCION DE UN NUEVO CONOCIMIENTO CRÍTICO</h4>

	<ul>
		<li>Generar procesos de formación política y capacitación técnica, orientadas a consolidar el conjunto de transformaciones sociales, políticas y económicas, que caracterizan la construcción del Estado Plurinacional.</li>
	</ul>

	<h4>FORTALECIMIENTO DE LA ECONOMÍA COMUNITARIA</h4>

	<ul>
		<li>Fortalecer la economía comunitaria, los sistemas agroalimentarios/artesanales, a través de la provisión de asistencia técnica orientada a la gestión de la agro biodiversidad y la seguridad alimentaria.</li>
	</ul>

	<h4>IMPLEMENTACION DE UN MODELO DE SERVICIOS EN SALUD POPULAR Y COMUNITARIA</h4>

	<ul>
		<li>Desarrollar acciones modélicas de salud popular- comunitaria, a través de prestación de servicios con calidad y calidez, accesible a los sectores más vulnerables, recuperando practicas de medicina tradicional para un sistema de salud intercultural.</li>
	</ul>

	<h4>FORTALECIMIENTO DE LOS SISTEMAS ADMINISTRATIVOS, PLANIFICACIÓN, SEGUIMIENTO Y EVALUACIÓN</h4>

	<ul>
		<li>Fortalecer los sistemas: Político, Jurídico Normativo, Administrativo, Financiero y de Fiscalización y Control.</li>
	</ul>

