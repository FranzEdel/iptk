	<h1>Visión</h1>
	<p>Contribuir a la emancipación social, política y económica de los sectores sociales más vulnerables en el marco de la construcción del Estado Plurinacional.</p>
	 
	<h1>Misión</h1>
	<p>Generar capacidades, prestar servicios y asistencia técnica a hombres y mujeres de los sectores sociales más vulnerables de Potosí y Chuquisaca, en los ámbitos del conocimiento o saber; humano – social; y económico productivo, con enfoque de género, interculturalidad, sostenibilidad ambiental y seguridad/soberanía alimentaria, para contribuir al mejoramiento de la calidad de vida.</p>
	 
	<h1>Mandatos</h1>
	<p>Toda acción institucional en la historia del IPTK, está caracterizada por una profunda y mística de trabajo expresada en mandados - responsabilidades legales y morales (valores) - compartidos por todo el personal, construidos y revisados de manera colectiva.</p>

	<ol>
		<li>Opción política por los pobres:</li>
		<ul>
			<li>Vocación de Servicio: Trabajamos de forma desinteresada y consecuente, brindando todas nuestras capacidades y posibilidades con la mayor predisposición.</li>
			<li>Solidaridad: Compartimos, ayudamos y nos identificamos con los sectores pobres.</li>
			<li>Respeto: Reconocemos valores de la gente y los aceptamos como son en su pluralidad, decisiones y libertad.</li>
			<li>Honestidad: Demostramos honradez y transparencia en nuestra conducta individual e institucional.</li>
			<li>Lealtad: Somos fieles y consecuentes con los principios de la Institución y con los actores sociales pobres.</li>
		</ul>
		<li>Defensa de los Derechos Humanos:</li>
		<ul>
			<li>Defendemos y respetamos los valores, principios y derechos individuales y colectivos, consagrados por la constitución política del Estado Plurinacional.</li>
			<li>Representan para el IPTK un proceso de construcción ciudadana, de reconocimiento, respeto y valoración de lo diverso y plural pero con sentido de oportunidad para todos.</li>
			<li>Se da énfasis a los derechos a: a la vida, la niñez, derecho humano a una alimentación adecuada, derecho a la salud y a la educación, a la no discriminación, a la libertad de opinión, al trabajo.</li>
		</ul>
		<li>Respeto al Medio Ambiente:</li>
		<ul>
			<li>Promover y garantizar el desarrollo sostenible del medio ambiente, a través del aprovechamiento responsable y planificado de los recursos naturales, y del desarrollo y fortalecimiento de la base productiva en sus diferentes dimensiones y niveles.</li>
			<li>Promover la conservación, protección y mejora de la calidad del medio ambiente y la biodiversidad, para el bienestar de las generaciones actuales y futuras.</li>
			<li>Promover acciones y políticas de mitigación a fin de reducir la vulnerabilidad hacia las amenazas climáticas.</li>
		</ul>
		<li>Trabajar por la Equidad e Igualdad de Género y Generacional:</li>
		<ul>
			<li>El IPTK entiende y aplica la equidad e igualdad de género y generacional, como la necesidad de superar toda situación de marginación y violencia contra la mujer, niños/as, jóvenes y adultos mayores.</li>
			<li>El IPTK en toda su estructura institucional, aplica y promueve políticas de equidad e igualdad de género y generacional, con el objetivo del logro de relaciones equitativas entre hombres y mujeres.</li>
		</ul>
		<li>Promover la Interculturalidad:</li>
		<ul>
			<li>Reconociendo que Bolivia es un país pluricultural, la interacción entre culturas de las comunidades originarias y de los migrantes es motivo de la integración y convivencia en el accionar institucional del IPTK, en una relación basada en el respeto a la diversidad y el enriquecimiento mutuo.</li>
			<li>Tenemos como horizonte promover el dialogo y la relación entre culturas y no solamente su reconocimiento y visibilización social. La interculturalidad para el IPTK es pilar fundamental desde hace 36 años, como una interacción comunicativa que se produce entre dos o más grupos humanos de diferente cultura, permitiendo acciones corporativas tolerantes y flexibles en el dialogo y la relación de unos con otros.</li>
			<li>El IPTK promueve en su personal actitudes de compromiso social, político, respeto, tolerancia y valoración de la diferencia.</li>
		</ul>
	</ol>
	
